#二、遍历元组中的所有值
dimensions = ('祈双','菌酱酱')
print('\n')
for dimension in dimensions:
    print(dimension)

#三、修改元组变量
dimensions = ('祈双','菌酱酱')
print('\n是谁:')
for dimension in dimensions:
    print(dimension)

